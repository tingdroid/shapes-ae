package geomlab;


/**
 * A command that may be appear on a menu. Static methods of this class create
 * the menus for the application
 */
public abstract class Command {

	public abstract void perform() throws CommandException;

	public static class CommandException extends Exception {
		private String errtag;

		public CommandException(String message, String errtag) {
			super(message);
			this.errtag = errtag;
		}

		public String getErrtag() {
			return errtag;
		}
	}

}